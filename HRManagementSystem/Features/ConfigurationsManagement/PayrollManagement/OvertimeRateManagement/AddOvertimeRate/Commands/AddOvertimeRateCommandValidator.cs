using FluentValidation;

namespace HRManagementSystem.Features.ConfigurationsManagement.PayrollManagement.OvertimeRateManagement.AddOvertimeRate.Commands
{
    public class AddOvertimeRateCommandValidator : AbstractValidator<AddOvertimeRateCommand>
    {
        public AddOvertimeRateCommandValidator()
        {

            RuleFor(x => x.Name)
                .NotEmpty().WithMessage("??? ??? ????? ??????? ?????.")
                .MinimumLength(3).WithMessage("??? ?? ???? ????? ??? ????? 3 ????.")
                .MaximumLength(100).WithMessage("????? ?? ??? ?? ?????? 100 ???.");

            // 2. ?????? ?? ????? ????? (Rate Factor)
            RuleFor(x => x.RateFactor)
                .GreaterThan(1.0m).WithMessage("??? ?? ???? ????? ????? ???? ?? 1.0 ????? ???????.");


            RuleFor(x => x.RateFactor)
                .Must(v => v * 100 % 1 == 0)
                .WithMessage("??? ?? ???? ????? ????? ??? ???? ??????? ???????.");


            RuleFor(x => x.Description)
                .MaximumLength(500).WithMessage("????? ?? ??? ?? ?????? 500 ???.");

        }
    }
}
